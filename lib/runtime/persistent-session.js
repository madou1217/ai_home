'use strict';

// Persistent sessions: run the provider CLI inside tmux so a session started
// locally survives the foreground client and can be re-attached later — start
// `aih claude 1` at your desk, walk away, SSH back into the same host, then
// choose it from `aih claude sessions` or use -M/-R to target the exact session.
//
// tmux IS the engine — we never reinvent a multiplexer. tmux's panes, copy-mode,
// scrollback, mouse and resilience are the whole point. We just wrap the launch.
//
// Addressing model — the part that fixes single-account lock-in:
//   * socket  = per ACCOUNT  (`tmux -L aih-<provider>-<accountRef>`). One tmux server per
//     account, created with that account's fully isolated env, so credentials
//     never cross account boundaries. Secrets ride the process env ONLY — never
//     tmux `-e`/argv, which would leak to `ps`.
//   * session = a fresh per-launch project session or an explicit LABEL. A
//     single account server therefore holds MANY independent sessions. Bare
//     launches never select an existing process; sessions / -M / -R are the
//     explicit paths for entering, mirroring, or taking over one.
//
// Cross-platform: tmux is the engine on macOS / Linux / WSL, and on native
// Windows via a tmux-compatible binary — `psmux` (native ConPTY, speaks tmux's
// CLI) or an MSYS2/Cygwin `tmux.exe`. detectTmux() finds whichever is present.


function getSessionIncompatibility(session, options = {}) {
  if (!session) return '';
  if (session.paneDeadChecked && session.paneDead) return 'pane-dead';
  if (session.screenCompletedChecked && session.screenCompleted) return 'screen-completed';
  if (
    options.requireUtf8Runtime
    && session.utf8RuntimeChecked
    && !isUtf8RuntimeReadySession(session)
  ) return 'utf8-runtime';
  if (
    options.requireClaudeRenderRuntime
    && session.claudeRenderRuntimeChecked
    && !isClaudeRenderRuntimeReadySession(session)
  ) return 'claude-render-runtime';
  if (
    options.requirePsmuxCodexLaunchRuntime
    && session.psmuxCodexLaunchRuntimeChecked
    && !isPsmuxCodexLaunchRuntimeReadySession(session)
  ) return 'psmux-codex-launch-runtime';
  if (
    options.requireProviderSupervisorRuntime
    && session.providerSupervisorRuntimeChecked
    && !isProviderSupervisorRuntimeReadySession(session)
  ) return 'provider-supervisor-runtime';
  return '';
}

function isSessionCompatible(session, options = {}) {
  return !getSessionIncompatibility(session, options);
}

function buildSessionCompatibilityOptions(options = {}) {
  const cliName = String(options.cliName || '').trim().toLowerCase();
  const isLogin = options.isLogin === true;
  return {
    requireUtf8Runtime: options.requireUtf8Runtime !== false,
    requireClaudeRenderRuntime: cliName === 'claude' && !isLogin,
    requirePsmuxCodexLaunchRuntime: cliName === 'codex'
      && !isLogin
      && options.nativeWindowsPsmux === true,
    requireProviderSupervisorRuntime: !isLogin
      && options.gateway !== true
      && options.usesAuthProjection === true
  };
}

function parseSessionList(stdout) {
  return String(stdout || '')
    .split('\n')
    .map((line) => line.replace(/\r$/, ''))
    .filter((line) => line.trim())
    .map((line) => {
      const [
        name,
        attached,
        created,
        sessionPath,
        title,
        windowName,
        command,
        panePid,
        utf8Runtime,
        claudeRenderRuntime,
        paneDead,
        psmuxCodexLaunchRuntime,
        paneCurrentPath,
        providerSupervisorRuntime
      ] = splitSessionListLine(line);
      return {
        name: String(name || '').trim(),
        attached: String(attached || '0').trim() !== '0',
        created: Number(created) || 0,
        // Display path follows the pane's CURRENT cwd; startPath keeps the
        // session's launch directory — the session registry must record the
        // latter so a post-reboot restore recreates the session (and resumes
        // the provider conversation) in the project root, not wherever the
        // user last cd'd to.
        path: String(paneCurrentPath || sessionPath || '').trim(),
        startPath: String(sessionPath || '').trim(),
        title: compactSessionText(title),
        windowName: compactSessionText(windowName),
        command: compactSessionText(command),
        panePid: Number(panePid) || 0,
        utf8Runtime: compactSessionText(utf8Runtime),
        utf8RuntimeChecked: typeof utf8Runtime !== 'undefined',
        utf8RuntimeReady: String(utf8Runtime || '').trim() === UTF8_RUNTIME_MARKER_VALUE,
        claudeRenderRuntime: compactSessionText(claudeRenderRuntime),
        claudeRenderRuntimeChecked: typeof claudeRenderRuntime !== 'undefined',
        claudeRenderRuntimeReady: String(claudeRenderRuntime || '').trim() === CLAUDE_RENDER_RUNTIME_MARKER_VALUE,
        paneDead: String(paneDead || '').trim() === '1',
        paneDeadChecked: typeof paneDead !== 'undefined',
        psmuxCodexLaunchRuntime: compactSessionText(psmuxCodexLaunchRuntime),
        psmuxCodexLaunchRuntimeChecked: typeof psmuxCodexLaunchRuntime !== 'undefined',
        psmuxCodexLaunchRuntimeReady: String(psmuxCodexLaunchRuntime || '').trim() === PSMUX_CODEX_LAUNCH_RUNTIME_MARKER_VALUE,
        providerSupervisorRuntime: compactSessionText(providerSupervisorRuntime),
        providerSupervisorRuntimeChecked: typeof providerSupervisorRuntime !== 'undefined',
        providerSupervisorRuntimeReady: String(providerSupervisorRuntime || '').trim() === PROVIDER_SUPERVISOR_RUNTIME_MARKER_VALUE
      };
    })
    .filter((s) => s.name);
}

// Decide how one requested session should be handled. The caller must declare
// its intent explicitly:
//   create — strict fresh create; never reuse existing state
//   select — enter this exact existing target; never create or substitute
//   upsert — named create-or-enter behavior
// Returns { session, action }:
//   'new'          — create the requested session
//   'reattach'     — the session exists but its previous client is gone (the
//                    reconnect case: closed terminal / dropped SSH) → land back in
//   'takeover'     — an explicitly named (-S) session is live elsewhere; the user
//                    targeted it by name, so detach the other client and take over
//   'mirror'       — share mode (-M): the target is live elsewhere, attach to it
//                    ALONGSIDE the other client — both mirror the same session,
//                    neither is kicked
//   'new-completed' — the target pane is dead/completed, so create a fresh
//                    sibling instead of attaching to an input-dead pane
// select/upsert + options.share chooses mirror-over-takeover when the target is
// live. Only create is forbidden from entering existing state.
function planPersistentSession(sessions, baseSession, options = {}) {
  const intent = String(options.intent || '').trim();
  if (!['create', 'select', 'upsert'].includes(intent)) {
    const error = new Error('invalid_persistent_session_intent');
    error.code = 'invalid_persistent_session_intent';
    throw error;
  }
  const share = !!options.share;
  const compatibilityOptions = {
    requireUtf8Runtime: !!options.requireUtf8Runtime,
    requireClaudeRenderRuntime: !!options.requireClaudeRenderRuntime,
    requirePsmuxCodexLaunchRuntime: !!options.requirePsmuxCodexLaunchRuntime,
    requireProviderSupervisorRuntime: !!options.requireProviderSupervisorRuntime
  };
  const list = Array.isArray(sessions) ? sessions : [];
  if (intent === 'create') return { session: baseSession, action: 'new' };

  const byName = new Map(list.map((s) => [s.name, s]));
  const base = byName.get(baseSession);
  const incompatibility = getSessionIncompatibility(base, compatibilityOptions);
  const incompatibleAction = () => (
    /^(?:pane-dead|screen-completed)$/.test(incompatibility)
      ? 'new-completed'
      : 'new-compatible'
  );
  const nextReplacementPlan = (fallbackAction) => {
    const prefix = `${baseSession}-`;
    let maxN = 1;
    for (const s of list) {
      if (!s.name.startsWith(prefix)) continue;
      const n = Number(s.name.slice(prefix.length));
      if (!Number.isInteger(n) || n < 2) continue;
      if (n > maxN) maxN = n;
    }
    return { session: `${baseSession}-${maxN + 1}`, action: fallbackAction };
  };

  if (!base) {
    return intent === 'select'
      ? { session: baseSession, action: 'missing' }
      : { session: baseSession, action: 'new' };
  }
  if (incompatibility) {
    return intent === 'select'
      ? { session: baseSession, action: 'unavailable', reason: incompatibility }
      : nextReplacementPlan(incompatibleAction());
  }
  if (!base.attached) return { session: baseSession, action: 'reattach' };
  return { session: baseSession, action: share ? 'mirror' : 'takeover' };
}

// Turn the raw session list into a display model grouped into "this project" vs
// "other projects", each row carrying a ready-to-run command. `cwd` is where the
// user ran `sessions` from, so we can group sessions by project. Pure +
// testable; the router owns interactive exact-target selection.
function describeSessionList(sessions, options = {}) {
  const cliName = String(options.cliName || '').trim();
  const cliSelector = String(options.cliSelector || '').trim();
  const cwd = String(options.cwd || '').trim();
  const shareLive = options.shareLive !== false;
  const pickerCommand = `aih ${cliName} sessions ${cliSelector}`;
  const base = cwd ? deriveSessionName({ cwd }) : '';
  const normPath = (p) => String(p || '').replace(/\/+$/, '');
  const cwdNorm = normPath(cwd);
  // A session belongs to "this project" if it shares the project's generated
  // prefix (name match) OR it was started in this exact directory (path match,
  // covers `-S`-named sessions launched here).
  const belongsToCwd = (s) => Boolean(base) && (
    s.name === base
    || s.name.startsWith(`${base}-`)
    || (cwdNorm && normPath(s.path) === cwdNorm)
  );

  const row = (s) => {
    const completed = Boolean(
      (s.paneDeadChecked && s.paneDead)
      || (s.screenCompletedChecked && s.screenCompleted)
    );
    const live = s.attached && !completed;
    const here = belongsToCwd(s);
    const legacyUtf8Runtime = Boolean(s.utf8RuntimeChecked && !isUtf8RuntimeReadySession(s));
    const legacyClaudeRenderRuntime = cliName === 'claude'
      && Boolean(s.claudeRenderRuntimeChecked && !isClaudeRenderRuntimeReadySession(s));
    const legacyPsmuxCodexLaunchRuntime = cliName === 'codex'
      && Boolean(s.psmuxCodexLaunchRuntimeChecked && !isPsmuxCodexLaunchRuntimeReadySession(s));
    let command;
    if (legacyUtf8Runtime || legacyClaudeRenderRuntime || legacyPsmuxCodexLaunchRuntime || completed) {
      // Do not recommend mirror/resume commands for panes created before the
      // current runtime markers, or for completed/dead panes. A normal launch
      // lets the planner create a clean sibling instead of re-entering a pane
      // that cannot accept input.
      if (here) command = `aih ${cliName} ${cliSelector}`;
      else if (s.path) command = `cd ${shellQuote(s.path)} && aih ${cliName} ${cliSelector}`;
      else command = `（在该项目目录里运行 aih ${cliName} ${cliSelector}）`;
    } else if (s.name.startsWith('s-')) {
      // Explicitly named session: addressable from anywhere by its label. Use
      // mirror mode for live sessions so list guidance never suggests stealing.
      command = `aih ${cliName} ${cliSelector} ${shareLive ? '-M' : '-R'} -S ${s.name.slice(2)}`;
    } else {
      // Auto-generated names are exact identities. A bare provider launch is
      // always fresh, so existing sessions must be selected through the picker.
      command = pickerCommand;
    }
    const description = deriveSessionDescription(s);
    return {
      name: s.name,
      live,
      completed,
      here,
      path: s.path,
      command,
      description,
      agentSessionId: compactSessionText(s.agentSessionId),
      targetSession: s.name
    };
  };

  const rows = sessions.map(row);
  return {
    here: rows.filter((r) => r.here),
    others: rows.filter((r) => !r.here),
    cwd,
    hasCwd: Boolean(base)
  };
}

// Build the argv that lists an account's live persistent sessions, one per line
// as `<name>\t<attached>\t<created>\t<path>` — used by the `aih ... sessions`
// surface so a returning user can see and re-attach what is running.
function buildListSessionsCommand(options = {}) {
  const command = options.tmuxCommand || options.tmuxPath || 'tmux';
  const socket = deriveSocket(options.cliName, options.runtimeScope);
  const format = [
    '#{session_name}',
    '#{session_attached}',
    '#{session_created}',
    '#{session_path}',
    '#{pane_title}',
    '#{window_name}',
    '#{pane_current_command}',
    '#{pane_pid}',
    `#{E:${UTF8_RUNTIME_MARKER_KEY}}`,
    `#{E:${CLAUDE_RENDER_RUNTIME_MARKER_KEY}}`,
    '#{pane_dead}',
    `#{E:${PSMUX_CODEX_LAUNCH_RUNTIME_MARKER_KEY}}`,
    '#{pane_current_path}',
    `#{E:${PROVIDER_SUPERVISOR_RUNTIME_MARKER_KEY}}`
  ].join(SESSION_LIST_SEPARATOR);
  return {
    command,
    args: [
      ...buildTmuxBaseArgs(socket),
      'list-sessions',
      '-F', format
    ],
    socket
  };
}

function buildKillSessionCommand(options = {}) {
  const command = options.tmuxCommand || options.tmuxPath || 'tmux';
  const socket = deriveSocket(options.cliName, options.runtimeScope);
  const session = String(options.sessionName || options.targetSession || '').trim();
  if (!isSafeSessionName(session)) return null;
  return {
    command,
    args: [
      ...buildTmuxBaseArgs(socket),
      'kill-session',
      '-t', session
    ],
    socket,
    session
  };
}


const TRANSPARENT_CONF = [
  '# Auto-generated by ai-home. Keeps tmux invisible under the aih runtime.',
  'set -g status off',
  'set -g window-size latest',
  // Keep aggressive-resize OFF (explicitly, not by omission, so re-sourcing this
  // file into an already-running server actively resets a previously-on value).
  // One aih account socket hosts MANY sessions (per project) and mirror mode
  // (-M) attaches MULTIPLE clients to one session. tmux's manual warns
  // aggressive-resize is "poor for interactive programs such as shells" and that
  // "the window may resize when the current window is changed on another
  // session". Under our multi-session / multi-client server that means another
  // session's activity silently resizes the live window out from under an
  // interactive TUI, which redraws incrementally and leaves stale cells (the
  // stray glyphs in column 1 that a manual terminal resize — a full SIGWINCH
  // redraw — wipes clean). Off keeps each window pinned to its attached client.
  'set -g aggressive-resize off',
  'set -g escape-time 0',
  'set -gq allow-passthrough on',
  // Inner panes (Codex-style TUIs) may request extended keys themselves; using
  // "on" forwards those requests to the outer terminal only when a pane asks for
  // it, instead of "always" which unconditionally puts the outer SSH terminal into
  // CSI-u mode. "always" causes Chinese/CJK input bytes to be re-encoded as
  // CSI-u escape sequences by the SSH client, which breaks multibyte input in
  // terminals that support the Kitty keyboard protocol.
  'set -g extended-keys on',
  'set -g extended-keys-format csi-u',
  // Claude Code's fullscreen renderer updates large alternate-screen regions
  // during scroll. Let tmux batch those frames when the outer xterm-compatible
  // terminal supports synchronized updates, otherwise stale edge cells can
  // become visible during rapid redraw.
  'set -g terminal-features[0] "xterm*:clipboard:ccolour:cstyle:focus:title:extkeys:sync"',
  'set -gqu terminal-features[3]',
  'set -gqu terminal-features[4]',
  'set -gqu terminal-features[5]',
  'set -g mouse on',
  'set -g history-limit 50000',
  'set -g destroy-unattached off',
  'set -g detach-on-destroy on',
  ''
].join('\n');


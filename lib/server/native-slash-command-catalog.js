// 各 provider 内置 slash 命令的静态清单(仅供 webUI autocomplete;权威永远是 CLI 本身,
// 未列出的命令由 native-slash-commands.js 的 passthrough 放行给 CLI)。
//
// 来源与校验方式(2026-07-04 实测):在 tmux 里起各家真实 TUI,输入 "/" 抓自动补全面板全量,
// claude 另与官方 commands 文档求交集(本机技能/插件不进清单)。验证版本:
//   claude 2.1.191 · codex 0.142.0 · opencode 1.4.7 · agy(antigravity)当日最新 · gemini(文档,已弃用)
// 更新方式:重跑同样的 TUI 抓取(见 docs/fabric/21-provider-hooks.md 的 slash 附录)后增删条目。

function normalizeEntries(entries, source) {
  return entries.map((entry) => ({
    argumentHint: '',
    aliases: [],
    source,
    ...entry
  }));
}

